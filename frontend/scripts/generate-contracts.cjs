const fs = require("fs");
const path = require("path");

const root = path.resolve(__dirname, "../..");
const out = path.resolve(__dirname, "../src/contracts/generated.js");

const names = [
  "RobinIndexVault",
  "ReceiptToken",
  "FeeTreasury",
  "MockStockOracle",
  "RewardDistributor",
];

const ignored = new Set(["node_modules", ".git", "dist", ".vercel"]);

function walk(dir, found = []) {
  if (!fs.existsSync(dir)) return found;
  for (const entry of fs.readdirSync(dir, { withFileTypes: true })) {
    if (ignored.has(entry.name)) continue;
    const full = path.join(dir, entry.name);
    if (entry.isDirectory()) walk(full, found);
    else if (entry.isFile() && entry.name.endsWith(".json")) found.push(full);
  }
  return found;
}

function readJson(file) {
  try {
    return JSON.parse(fs.readFileSync(file, "utf8"));
  } catch {
    return null;
  }
}

const files = walk(root);
const result = {};

for (const name of names) {
  const matches = [];

  for (const file of files) {
    const json = readJson(file);
    if (!json || !Array.isArray(json.abi)) continue;

    const base = path.basename(file).toLowerCase();
    const contractName = String(json.contractName || "").toLowerCase();

    if (contractName === name.toLowerCase() || base.includes(name.toLowerCase())) {
      matches.push({ file, abi: json.abi });
    }
  }

  if (!matches.length) {
    console.warn(`WARN: ABI not found for ${name}`);
    result[name] = [];
    continue;
  }

  matches.sort((a, b) => b.abi.length - a.abi.length);
  result[name] = matches[0].abi;
  console.log(`OK: ${name} ABI from ${path.relative(root, matches[0].file)}`);
}

const content = `// Auto-generated by scripts/generate-contracts.cjs
// Do not edit manually.

export const ABIS = ${JSON.stringify(result, null, 2)};
`;

fs.writeFileSync(out, content);
console.log(`Generated ${path.relative(process.cwd(), out)}`);
